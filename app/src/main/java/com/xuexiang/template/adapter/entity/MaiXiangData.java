package com.xuexiang.template.adapter.entity;

public class MaiXiangData {
    String name;
    String x;
    String y;

    public MaiXiangData(String name, String x, String y) {
        this.name = name;
        this.x = x;
        this.y = y;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setX(String x) {
        this.x = x;
    }

    public void setY(String y) {
        this.y = y;
    }

    public String getName() {
        return name;
    }

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }
}
